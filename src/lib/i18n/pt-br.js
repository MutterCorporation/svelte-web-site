// Fun√ß√£o para obter texto com fallback
export function gettext(path, fallback = '') {
  const keys = path.split('.');
  /** @type {any} */
  let current = TEXTS_PT_BR;
  
  for (const key of keys) {
    if (current && typeof current === 'object' && key in current) {
      current = current[key];
    } else {
      return fallback || path;
    }
  }
  
  return typeof current === 'string' ? current : fallback || path;
}

// Fun√ß√£o para formatar texto com vari√°veis
export function formatText(path, variables = {}, fallback = '') {
  let text = gettext(path, fallback);
  
  // Substituir vari√°veis no formato {variavel}
  Object.entries(variables).forEach(([key, value]) => {
    const regex = new RegExp(`{${key}}`, 'g');
    text = text.replace(regex, String(value));
  });
  
  return text;
}

